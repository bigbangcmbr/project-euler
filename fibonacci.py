# Fibonacci


def fib(n):
    # Generate n'th fib

    if n <= 0:
        return 0
    if n == 1:
        return 1

    a, b = 0, 1

    for _ in range(2, n):
        b = a + b
        a = b - a

    return a + b


print(f"300th Fibonacci number : {fib(300)}")

